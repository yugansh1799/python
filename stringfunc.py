# String methods in python
name="deepansh kumar"
# 1. length function 2. lower method- use to print a string in lower method 3. upper method 4. title method 5. count method
# 1. len() function
print(len(name))
# 2. lower method
print(name.lower())
# 3. upper method - use to print all letters in uppercase.
print(name.upper())
# 4. title method - only first letter of string remains capital rest all remains small.
print(name.title())
# 5. count method - counts the no of time a character is present in the string.
print(name.count("e"))